  CREATE OR REPLACE EDITIONABLE PROCEDURE "SEBA"."LICZENIEKOSZTUZLECENIA" 
is
        order_price Number;
        
begin
  update repair_order ro set ro.price = 0;
  for i in (select * from order_and_repair)
  loop
        
        select price_of_defect into order_price from repair where repair_id = i.REPAIR_ID;
        update repair_order ro set ro.price = (ro.price + order_price) where ro.order_id = i.order_id;
        DBMS_OUTPUT.PUT_LINE('order_id = ' || i.order_id || ', repair_id = ' || i.repair_id || ', price_of_defect = ' || order_price);
  end loop;
end;


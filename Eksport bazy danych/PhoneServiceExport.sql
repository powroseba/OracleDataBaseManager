  CREATE OR REPLACE EDITIONABLE PROCEDURE "SEBA"."SETBONUSS" (bonus_salary IN NUMBER,
                    count_of_employers_for_bonus IN NUMBER)
is
    adequate_salary NUMBER;
begin 
    SELECT min(salary) into adequate_salary FROM employee WHERE rownum <= count_of_employers_for_bonus ORDER BY salary desc;
    update employee e set e.salary = e.salary + bonus_salary where e.salary >= adequate_salary; 

end;

